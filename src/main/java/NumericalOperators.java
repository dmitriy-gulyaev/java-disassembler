class NumericalOperators {

  int prefixIncrementOperator(int i) {
    return ++i;
  }

  int postfixIncrementOperator(int i) {
    return i++;
  }

  int prefixDecrementOperator(int i) {
    return --i;
  }

  int postfixDecrementOperator(int i) {
    return i--;
  }

  int additivePlusOperator(int i) {
    return i + 55;
  }

  int additiveMinusOperator(int i) {
    return i - 55;
  }

}
